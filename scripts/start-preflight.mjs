#!/usr/bin/env node
// Preflight for `pnpm run start`: prepare a production-mode local run from
// source. Reuses the dev preflight machinery (Docker, Postgres, Mix deps,
// migrations) but runs Phoenix in MIX_ENV=prod and auto-generates
// `server/.env` with sensible defaults so first-time users don't hand-edit
// any files. Idempotent on subsequent runs.

import { existsSync, readFileSync, writeFileSync } from 'node:fs'
import { dirname, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'

import {
  createLogger,
  ensureDockerRunning,
  ensureMixBootstrapTools,
  ensureMixDeps,
  ensurePhoenixAssets,
  ensurePnpmDeps,
  ensurePostgresUp,
  ensureRequiredToolchain,
  ensureSchema,
  host,
  quiet,
  streamRun,
} from './lib/preflight-utils.mjs'

const scriptDir = dirname(fileURLToPath(import.meta.url))
const repoRoot = resolve(scriptDir, '..')
const composeFile = resolve(repoRoot, 'server/docker-compose.yml')
const composeScript = resolve(scriptDir, 'docker-compose.mjs')
const clientDir = resolve(repoRoot, 'client')
const serverDir = resolve(repoRoot, 'server')
const containerName = 'xero-postgres'

const SERVER_ENV_FILE = resolve(serverDir, '.env')
const SERVER_ENV_EXAMPLE = resolve(serverDir, '.env.example')

const logger = createLogger('start')

const MIX_ENV_PROD = { ...process.env, MIX_ENV: 'prod', XERO_LAUNCH_MODE: 'local-source' }

function ensurePlatformSupported() {
  if (host !== 'darwin') {
    logger.fail(
      "`pnpm start` currently supports macOS only — use `pnpm dev` on this platform for now.",
    )
    process.exit(1)
  }
}

function generateSecretKeyBase() {
  const result = quiet('mix', ['phx.gen.secret'], { cwd: serverDir })
  if (result.status !== 0) {
    logger.fail('Could not generate SECRET_KEY_BASE via `mix phx.gen.secret`. Check Elixir/Mix install.')
    process.exit(1)
  }
  // mix prints the secret on the last non-empty line.
  const lines = result.stdout.split('\n').map((line) => line.trim()).filter(Boolean)
  const secret = lines.at(-1) ?? ''
  if (!/^[A-Za-z0-9+/=_-]{32,}$/.test(secret)) {
    logger.fail(`Unexpected SECRET_KEY_BASE format from mix: ${secret}`)
    process.exit(1)
  }
  return secret
}

function buildDefaultServerEnv(secretKeyBase) {
  return [
    '# Generated by `pnpm start` — auto-populated for local-source runs.',
    '# Edit via the in-app onboarding "Local environment" step.',
    '',
    '# --- Phoenix endpoint ---',
    'PHX_HOST=127.0.0.1',
    'PORT=4000',
    `SECRET_KEY_BASE=${secretKeyBase}`,
    '',
    '# --- Database ---',
    'DATABASE_URL=ecto://postgres:postgres@localhost/xero_prod',
    'POOL_SIZE=10',
    'ECTO_IPV6=',
    '',
    '# --- DNS clustering ---',
    'DNS_CLUSTER_QUERY=',
    '',
    '# --- CORS ---',
    'CORS_ORIGINS=http://localhost:3000,http://127.0.0.1:3000,tauri://localhost',
    '',
    '# --- Rate limiting ---',
    'RATE_LIMIT_PER_MINUTE=60',
    '',
    '# --- Oban ---',
    'OBAN_QUEUES=default:10,mailers:5',
    '',
  ].join('\n')
}

async function ensureServerEnv() {
  if (existsSync(SERVER_ENV_FILE)) {
    logger.log('server/.env already present — skipping generation.')
    return
  }

  if (!existsSync(SERVER_ENV_EXAMPLE)) {
    logger.fail(`Missing template at ${SERVER_ENV_EXAMPLE}; cannot bootstrap server/.env.`)
    process.exit(1)
  }

  logger.log('Generating server/.env with auto-derived defaults...')
  const secretKeyBase = generateSecretKeyBase()
  writeFileSync(SERVER_ENV_FILE, buildDefaultServerEnv(secretKeyBase), { mode: 0o600 })
  logger.ok('Wrote server/.env (chmod 600).')
}

function readEnvKey(envPath, key) {
  if (!existsSync(envPath)) return null
  const contents = readFileSync(envPath, 'utf8')
  const re = new RegExp(`^${key}=(.*)$`, 'm')
  const match = contents.match(re)
  return match ? match[1].trim() : null
}

async function ensureNodeDeps() {
  await ensurePnpmDeps(logger, {
    label: 'root tooling',
    dir: repoRoot,
    requiredBins: ['concurrently'],
  })
  await ensurePnpmDeps(logger, {
    label: 'desktop client',
    dir: clientDir,
    requiredBins: ['tauri', 'vite'],
  })
}

async function ensureProdAssets() {
  // `mix assets.deploy` minifies/digests the Phoenix asset pipeline. It
  // depends on `assets.setup` having run (handled by ensurePhoenixAssets).
  // Skip if the digest manifest already exists.
  const digestPath = resolve(serverDir, 'priv/static/cache_manifest.json')
  if (existsSync(digestPath)) {
    logger.log('Phoenix prod assets already deployed.')
    return
  }
  logger.log('Compiling and digesting Phoenix assets for prod (`mix assets.deploy`)...')
  await streamRun('mix', ['assets.deploy'], { cwd: serverDir, env: MIX_ENV_PROD })
  logger.ok('Phoenix prod assets ready.')
}

async function main() {
  const t0 = Date.now()
  ensurePlatformSupported()
  ensureRequiredToolchain(logger)
  await ensureNodeDeps()
  await ensureMixBootstrapTools(logger)
  await ensureServerEnv()
  await ensureMixDeps(logger, { serverDir, mixEnv: 'prod' })
  await ensurePhoenixAssets(logger, { serverDir, mixEnv: 'prod' })
  await ensureProdAssets()
  await ensureDockerRunning(logger)
  await ensurePostgresUp(logger, { containerName, composeFile, composeScript })
  await ensureSchema(logger, { serverDir, mixEnv: 'prod' })

  // Surface the resolved port so `launch-built-app.mjs` can derive
  // `VITE_XERO_SERVER_URL` without re-parsing .env.
  const port = readEnvKey(SERVER_ENV_FILE, 'PORT') ?? '4000'
  process.stdout.write(`XERO_START_PORT=${port}\n`)

  logger.ok(`Start preflight complete in ${((Date.now() - t0) / 1000).toFixed(1)}s.`)
}

main().catch((err) => {
  logger.fail(err?.message ?? String(err))
  process.exit(1)
})
