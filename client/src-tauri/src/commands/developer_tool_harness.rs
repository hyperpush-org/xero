#[tauri::command]
pub fn developer_tool_synthetic_run<R: Runtime>(
    app: AppHandle<R>,
    state: State<'_, DesktopState>,
    request: DeveloperToolSyntheticRunRequestDto,
) -> CommandResult<DeveloperToolSyntheticRunResponseDto> {
    validate_non_empty(&request.project_id, "projectId")?;
    if request.calls.is_empty() {
        return Err(CommandError::user_fixable(
            "developer_tool_harness_no_calls",
            "Synthetic harness runs require at least one tool call.",
        ));
    }

    let repo_root = resolve_project_root(&app, state.inner(), &request.project_id)?;
    let tool_runtime = AutonomousToolRuntime::for_project(&app, state.inner(), &request.project_id)?;

    let agent_session_id = match request.agent_session_id.as_deref() {
        Some(value) if !value.trim().is_empty() => value.trim().to_owned(),
        _ => {
            let session = project_store::create_agent_session(
                &repo_root,
                &AgentSessionCreateRecord {
                    project_id: request.project_id.clone(),
                    title: format!("Tool harness — {}", now_timestamp()),
                    summary: "Created by the developer tool harness for synthetic dispatch."
                        .into(),
                    selected: false,
                },
            )?;
            session.agent_session_id
        }
    };

    let run_id = generate_runtime_run_id();
    let calls = request
        .calls
        .into_iter()
        .map(|call| {
            let tool_name = call.tool_name.trim();
            if tool_name.is_empty() {
                return Err(CommandError::invalid_request("toolName"));
            }
            Ok(AgentToolCall {
                tool_call_id: call
                    .tool_call_id
                    .filter(|value| !value.trim().is_empty())
                    .unwrap_or_else(generate_tool_call_id),
                tool_name: tool_name.to_owned(),
                input: call.input,
            })
        })
        .collect::<CommandResult<Vec<_>>>()?;

    let options_dto = request.options.unwrap_or_default();
    let options = SyntheticDispatchOptions {
        stop_on_failure: options_dto.stop_on_failure.unwrap_or(true),
        approve_writes: options_dto.approve_writes.unwrap_or(false),
        operator_approve_all: options_dto.operator_approve_all.unwrap_or(false),
    };

    let outcome = dispatch_synthetic_tool_calls(
        &repo_root,
        &request.project_id,
        agent_session_id,
        run_id,
        HARNESS_PROVIDER_ID.into(),
        HARNESS_MODEL_ID.into(),
        tool_runtime,
        calls,
        options,
    )?;

    Ok(DeveloperToolSyntheticRunResponseDto {
        run_id: outcome.run_id,
        agent_session_id: outcome.agent_session_id,
        stopped_early: outcome.stopped_early,
        had_failure: outcome.had_failure,
        results: outcome
            .results
            .into_iter()
            .map(|entry| DeveloperToolHarnessCallResultDto {
                tool_call_id: entry.tool_call_id,
                tool_name: entry.tool_name,
                ok: entry.ok,
                summary: entry.summary,
                output: entry.output,
            })
            .collect(),
    })
}

#[tauri::command]
pub fn developer_tool_dry_run<R: Runtime>(
    app: AppHandle<R>,
    state: State<'_, DesktopState>,
    request: DeveloperToolDryRunRequestDto,
) -> CommandResult<DeveloperToolDryRunResponseDto> {
    validate_non_empty(&request.project_id, "projectId")?;
    validate_non_empty(&request.tool_name, "toolName")?;

    let repo_root = resolve_project_root(&app, state.inner(), &request.project_id)?;
    let tool_runtime = AutonomousToolRuntime::for_project(&app, state.inner(), &request.project_id)?;
    let registry_options = ToolRegistryOptions {
        skill_tool_enabled: tool_runtime.skill_tool_enabled(),
        runtime_agent_id: RuntimeAgentIdDto::Engineer,
        ..ToolRegistryOptions::default()
    };
    let tool_registry = ToolRegistry::builtin_with_options(registry_options);

    let tool_call = AgentToolCall {
        tool_call_id: request
            .tool_call_id
            .filter(|value| !value.trim().is_empty())
            .unwrap_or_else(generate_tool_call_id),
        tool_name: request.tool_name,
        input: request.input,
    };
    let report = dry_run_tool_call(
        &tool_registry,
        &tool_runtime,
        &repo_root,
        &request.project_id,
        tool_call,
        request.operator_approved.unwrap_or(false),
    )?;

    Ok(DeveloperToolDryRunResponseDto {
        tool_call_id: report.tool_call_id,
        tool_name: report.tool_name,
        decoded: report.decoded,
        policy_decision: DeveloperToolPolicyDecisionDto {
            action: report.policy_decision.action.as_str().to_owned(),
            code: report.policy_decision.code,
            explanation: report.policy_decision.explanation,
            risk_class: report.policy_decision.risk_class,
            project_trust: report.policy_decision.project_trust,
            network_intent: report.policy_decision.network_intent,
            credential_sensitivity: report.policy_decision.credential_sensitivity,
            prior_observation_required: report.policy_decision.prior_observation_required,
            os_target: report.policy_decision.os_target,
        },
        sandbox_decision: report.sandbox_decision,
        sandbox_denied: report.sandbox_denied,
    })
}

fn generate_tool_call_id() -> String {
    let mut bytes = [0_u8; 8];
    rand::thread_rng().fill_bytes(&mut bytes);
    let hex: String = bytes.iter().map(|byte| format!("{byte:02x}")).collect();
    format!("dev-harness-{hex}")
}

#[derive(serde::Serialize, serde::Deserialize)]
struct StoredSequencePayload {
    calls: Vec<DeveloperToolHarnessCallDto>,
    #[serde(default)]
    options: Option<DeveloperToolHarnessRunOptionsDto>,
}

fn generate_sequence_id() -> String {
    let mut bytes = [0_u8; 12];
    rand::thread_rng().fill_bytes(&mut bytes);
    let hex: String = bytes.iter().map(|byte| format!("{byte:02x}")).collect();
    format!("dev-harness-seq-{hex}")
}

fn parse_stored_sequence(
    id: String,
    name: String,
    payload: &str,
    created_at: String,
    updated_at: String,
) -> CommandResult<DeveloperToolSequenceRecordDto> {
    let parsed: StoredSequencePayload = serde_json::from_str(payload).map_err(|error| {
        CommandError::system_fault(
            "developer_tool_sequence_payload_invalid",
            format!("Xero could not parse developer tool sequence `{id}` payload: {error}"),
        )
    })?;
    Ok(DeveloperToolSequenceRecordDto {
        id,
        name,
        calls: parsed.calls,
        options: parsed.options,
        created_at,
        updated_at,
    })
}

#[tauri::command]
pub fn developer_tool_sequence_list<R: Runtime>(
    app: AppHandle<R>,
    state: State<'_, DesktopState>,
) -> CommandResult<DeveloperToolSequenceListResponseDto> {
    let global_db_path = state.global_db_path(&app)?;
    let connection = open_global_database(&global_db_path)?;
    let mut statement = connection
        .prepare(
            "SELECT id, name, payload, created_at, updated_at \
             FROM developer_tool_sequences \
             ORDER BY updated_at DESC, name ASC",
        )
        .map_err(|error| {
            CommandError::system_fault(
                "developer_tool_sequence_list_failed",
                format!("Xero could not prepare the developer tool sequence query: {error}"),
            )
        })?;
    let rows = statement
        .query_map([], |row| {
            Ok((
                row.get::<_, String>(0)?,
                row.get::<_, String>(1)?,
                row.get::<_, String>(2)?,
                row.get::<_, String>(3)?,
                row.get::<_, String>(4)?,
            ))
        })
        .map_err(|error| {
            CommandError::system_fault(
                "developer_tool_sequence_list_failed",
                format!("Xero could not list developer tool sequences: {error}"),
            )
        })?;

    let mut sequences = Vec::new();
    for row in rows {
        let (id, name, payload, created_at, updated_at) = row.map_err(|error| {
            CommandError::system_fault(
                "developer_tool_sequence_list_failed",
                format!("Xero could not read a developer tool sequence row: {error}"),
            )
        })?;
        sequences.push(parse_stored_sequence(id, name, &payload, created_at, updated_at)?);
    }

    Ok(DeveloperToolSequenceListResponseDto { sequences })
}

#[tauri::command]
pub fn developer_tool_sequence_upsert<R: Runtime>(
    app: AppHandle<R>,
    state: State<'_, DesktopState>,
    request: DeveloperToolSequenceUpsertRequestDto,
) -> CommandResult<DeveloperToolSequenceRecordDto> {
    let name = request.name.trim().to_owned();
    if name.is_empty() {
        return Err(CommandError::invalid_request("name"));
    }
    if request.calls.is_empty() {
        return Err(CommandError::user_fixable(
            "developer_tool_sequence_no_calls",
            "Sequences must contain at least one tool call.",
        ));
    }

    let id = request
        .id
        .clone()
        .map(|value| value.trim().to_owned())
        .filter(|value| !value.is_empty())
        .unwrap_or_else(generate_sequence_id);

    let payload = StoredSequencePayload {
        calls: request.calls.clone(),
        options: request.options.clone(),
    };
    let payload_json = serde_json::to_string(&payload).map_err(|error| {
        CommandError::system_fault(
            "developer_tool_sequence_payload_serialize_failed",
            format!("Xero could not serialize the developer tool sequence: {error}"),
        )
    })?;

    let global_db_path = state.global_db_path(&app)?;
    let connection = open_global_database(&global_db_path)?;
    let now = now_timestamp();
    connection
        .execute(
            "INSERT INTO developer_tool_sequences (id, name, payload, created_at, updated_at) \
             VALUES (?1, ?2, ?3, ?4, ?4) \
             ON CONFLICT(id) DO UPDATE SET name = excluded.name, payload = excluded.payload, updated_at = excluded.updated_at",
            rusqlite::params![id, name, payload_json, now],
        )
        .map_err(|error| {
            CommandError::system_fault(
                "developer_tool_sequence_upsert_failed",
                format!("Xero could not upsert the developer tool sequence: {error}"),
            )
        })?;

    let (created_at, updated_at) = connection
        .query_row(
            "SELECT created_at, updated_at FROM developer_tool_sequences WHERE id = ?1",
            rusqlite::params![id],
            |row| Ok((row.get::<_, String>(0)?, row.get::<_, String>(1)?)),
        )
        .map_err(|error| {
            CommandError::system_fault(
                "developer_tool_sequence_read_back_failed",
                format!("Xero could not read back the upserted developer tool sequence: {error}"),
            )
        })?;

    Ok(DeveloperToolSequenceRecordDto {
        id,
        name,
        calls: request.calls,
        options: request.options,
        created_at,
        updated_at,
    })
}

#[tauri::command]
pub fn developer_tool_model_run<R: Runtime + 'static>(
    app: AppHandle<R>,
    state: State<'_, DesktopState>,
    request: DeveloperToolModelRunRequestDto,
) -> CommandResult<DeveloperToolModelRunResponseDto> {
    validate_non_empty(&request.project_id, "projectId")?;
    validate_non_empty(&request.tool_name, "toolName")?;
    validate_non_empty(&request.prompt, "prompt")?;

    let runtime_agent_id = match request.runtime_agent_id.as_deref() {
        None | Some("") | Some("engineer") => RuntimeAgentIdDto::Engineer,
        Some("ask") => RuntimeAgentIdDto::Ask,
        Some("plan") => RuntimeAgentIdDto::Plan,
        Some("crawl") => RuntimeAgentIdDto::Crawl,
        Some("debug") => RuntimeAgentIdDto::Debug,
        Some("agent_create") => RuntimeAgentIdDto::AgentCreate,
        Some(other) => {
            return Err(CommandError::user_fixable(
                "developer_tool_harness_invalid_agent",
                format!("Unknown runtime agent id `{other}`."),
            ));
        }
    };

    let repo_root = resolve_project_root(&app, state.inner(), &request.project_id)?;
    let agent_session_id = match request.agent_session_id.as_deref() {
        Some(value) if !value.trim().is_empty() => value.trim().to_owned(),
        _ => {
            let session = project_store::create_agent_session(
                &repo_root,
                &AgentSessionCreateRecord {
                    project_id: request.project_id.clone(),
                    title: format!("Tool harness (mode A) — {}", now_timestamp()),
                    summary: format!(
                        "Created by the developer tool harness to model-drive `{}`.",
                        request.tool_name
                    ),
                    selected: false,
                },
            )?;
            session.agent_session_id
        }
    };

    let prompt = format!(
        "Tool harness (mode A): exercise the `{}` tool. {}",
        request.tool_name, request.prompt,
    );

    let controls = RuntimeRunControlInputDto {
        runtime_agent_id,
        agent_definition_id: None,
        provider_profile_id: None,
        model_id: String::new(),
        thinking_effort: None,
        approval_mode: RuntimeRunApprovalModeDto::Suggest,
        plan_mode_required: false,
    };

    let snapshot = start_agent_task(
        app,
        state,
        StartAgentTaskRequestDto {
            project_id: request.project_id.clone(),
            agent_session_id: agent_session_id.clone(),
            prompt,
            controls: Some(controls),
        },
    )?;

    Ok(DeveloperToolModelRunResponseDto {
        run_id: snapshot.run_id,
        agent_session_id,
        project_id: request.project_id,
    })
}

