pub fn developer_tool_dry_run_service(
    repo_root: &Path,
    tool_runtime: &AutonomousToolRuntime,
    request: DeveloperToolDryRunRequestDto,
) -> CommandResult<DeveloperToolDryRunResponseDto> {
    validate_non_empty(&request.project_id, "projectId")?;
    validate_non_empty(&request.tool_name, "toolName")?;

    let registry_options = ToolRegistryOptions {
        skill_tool_enabled: tool_runtime.skill_tool_enabled(),
        runtime_agent_id: RuntimeAgentIdDto::Engineer,
        ..ToolRegistryOptions::default()
    };
    let tool_registry = ToolRegistry::builtin_with_options(registry_options);

    let tool_call = AgentToolCall {
        tool_call_id: request
            .tool_call_id
            .filter(|value| !value.trim().is_empty())
            .unwrap_or_else(generate_tool_call_id),
        tool_name: request.tool_name,
        input: request.input,
    };
    let report = dry_run_tool_call(
        &tool_registry,
        tool_runtime,
        repo_root,
        &request.project_id,
        tool_call,
        request.operator_approved.unwrap_or(false),
    )?;

    Ok(DeveloperToolDryRunResponseDto {
        tool_call_id: report.tool_call_id,
        tool_name: report.tool_name,
        decoded: report.decoded,
        policy_decision: DeveloperToolPolicyDecisionDto {
            action: report.policy_decision.action.as_str().to_owned(),
            code: report.policy_decision.code,
            explanation: report.policy_decision.explanation,
            risk_class: report.policy_decision.risk_class,
            project_trust: report.policy_decision.project_trust,
            network_intent: report.policy_decision.network_intent,
            credential_sensitivity: report.policy_decision.credential_sensitivity,
            prior_observation_required: report.policy_decision.prior_observation_required,
            os_target: report.policy_decision.os_target,
        },
        sandbox_decision: report.sandbox_decision,
        sandbox_denied: report.sandbox_denied,
    })
}

