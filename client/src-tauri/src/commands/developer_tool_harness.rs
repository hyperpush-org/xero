#[tauri::command]
pub fn developer_tool_harness_project<R: Runtime>(
    app: AppHandle<R>,
    state: State<'_, DesktopState>,
) -> CommandResult<DeveloperToolHarnessProjectDto> {
    let registry_path = state.global_db_path(&app)?;
    let app_data_dir = state.app_data_dir(&app)?;
    let repo_root = app_data_dir.join(HARNESS_FIXTURE_DIRECTORY);

    seed_harness_fixture_repo(&repo_root)?;
    ensure_harness_fixture_registered(&registry_path, &repo_root)?;

    Ok(DeveloperToolHarnessProjectDto {
        project_id: HARNESS_FIXTURE_PROJECT_ID.to_owned(),
        display_name: HARNESS_FIXTURE_DISPLAY_NAME.to_owned(),
        root_path: repo_root.to_string_lossy().into_owned(),
    })
}

fn seed_harness_fixture_repo(repo_root: &Path) -> CommandResult<()> {
    fs::create_dir_all(repo_root).map_err(|error| {
        CommandError::system_fault(
            "developer_tool_harness_fixture_dir_failed",
            format!(
                "Xero could not create the harness fixture directory at {}: {error}",
                repo_root.display()
            ),
        )
    })?;

    write_fixture_file(repo_root, "README.md", HARNESS_FIXTURE_README)?;
    write_fixture_file(repo_root, "sample.txt", HARNESS_FIXTURE_SAMPLE)?;

    Ok(())
}

fn write_fixture_file(repo_root: &Path, relative: &str, contents: &str) -> CommandResult<()> {
    let path = repo_root.join(relative);
    if let Some(parent) = path.parent() {
        fs::create_dir_all(parent).map_err(|error| {
            CommandError::system_fault(
                "developer_tool_harness_fixture_dir_failed",
                format!(
                    "Xero could not create the harness fixture parent at {}: {error}",
                    parent.display()
                ),
            )
        })?;
    }

    let needs_write = match fs::read_to_string(&path) {
        Ok(existing) => existing != contents,
        Err(_) => true,
    };

    if needs_write {
        fs::write(&path, contents).map_err(|error| {
            CommandError::system_fault(
                "developer_tool_harness_fixture_seed_failed",
                format!(
                    "Xero could not seed the harness fixture file at {}: {error}",
                    path.display()
                ),
            )
        })?;
    }

    Ok(())
}

fn ensure_harness_fixture_registered(
    registry_path: &Path,
    repo_root: &Path,
) -> CommandResult<PathBuf> {
    db::configure_project_database_paths(registry_path);

    let root_path_string = repo_root.to_string_lossy().into_owned();
    let repository = CanonicalRepository {
        project_id: HARNESS_FIXTURE_PROJECT_ID.to_owned(),
        repository_id: HARNESS_FIXTURE_REPOSITORY_ID.to_owned(),
        root_path: repo_root.to_path_buf(),
        root_path_string: root_path_string.clone(),
        common_git_dir: repo_root.join(".git"),
        display_name: HARNESS_FIXTURE_DISPLAY_NAME.to_owned(),
        branch_name: None,
        head_sha: None,
        branch: None,
        last_commit: None,
        status_entries: Vec::new(),
        has_staged_changes: false,
        has_unstaged_changes: false,
        has_untracked_changes: false,
        additions: 0,
        deletions: 0,
    };

    let imported = db::import_project_with_origin(
        &repository,
        ProjectOrigin::Greenfield,
        &Default::default(),
    )?;

    registry::upsert_project(
        registry_path,
        RegistryProjectRecord {
            project_id: imported.project.id.clone(),
            repository_id: imported.repository.id.clone(),
            root_path: imported.repository.root_path.clone(),
        },
        &Default::default(),
    )?;

    Ok(repo_root.to_path_buf())
}
