pub fn developer_tool_sequence_upsert_service(
    global_db_path: &Path,
    request: DeveloperToolSequenceUpsertRequestDto,
) -> CommandResult<DeveloperToolSequenceRecordDto> {
    let name = request.name.trim().to_owned();
    if name.is_empty() {
        return Err(CommandError::invalid_request("name"));
    }
    if request.calls.is_empty() {
        return Err(CommandError::user_fixable(
            "developer_tool_sequence_no_calls",
            "Sequences must contain at least one tool call.",
        ));
    }

    let id = request
        .id
        .clone()
        .map(|value| value.trim().to_owned())
        .filter(|value| !value.is_empty())
        .unwrap_or_else(generate_sequence_id);

    let payload = StoredSequencePayload {
        calls: request.calls.clone(),
        options: request.options.clone(),
    };
    let payload_json = serde_json::to_string(&payload).map_err(|error| {
        CommandError::system_fault(
            "developer_tool_sequence_payload_serialize_failed",
            format!("Xero could not serialize the developer tool sequence: {error}"),
        )
    })?;

    let connection = open_global_database(global_db_path)?;
    let now = now_timestamp();
    connection
        .execute(
            "INSERT INTO developer_tool_sequences (id, name, payload, created_at, updated_at) \
             VALUES (?1, ?2, ?3, ?4, ?4) \
             ON CONFLICT(id) DO UPDATE SET name = excluded.name, payload = excluded.payload, updated_at = excluded.updated_at",
            rusqlite::params![id, name, payload_json, now],
        )
        .map_err(|error| {
            CommandError::system_fault(
                "developer_tool_sequence_upsert_failed",
                format!("Xero could not upsert the developer tool sequence: {error}"),
            )
        })?;

    let (created_at, updated_at) = connection
        .query_row(
            "SELECT created_at, updated_at FROM developer_tool_sequences WHERE id = ?1",
            rusqlite::params![id],
            |row| Ok((row.get::<_, String>(0)?, row.get::<_, String>(1)?)),
        )
        .map_err(|error| {
            CommandError::system_fault(
                "developer_tool_sequence_read_back_failed",
                format!("Xero could not read back the upserted developer tool sequence: {error}"),
            )
        })?;

    Ok(DeveloperToolSequenceRecordDto {
        id,
        name,
        calls: request.calls,
        options: request.options,
        created_at,
        updated_at,
    })
}

