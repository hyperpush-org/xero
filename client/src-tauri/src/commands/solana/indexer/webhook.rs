fn render_readme(ctx: &ScaffoldContext<'_>) -> String {
    format!(
        r#"# {slug} — Helius webhook receiver

Express receiver for [Helius enhanced webhooks](https://www.helius.dev/docs/webhooks).
Normalises each incoming tx and dispatches to handlers in
`src/handlers.ts`.

## Run (local)

```sh
pnpm install
cp .env.example .env
pnpm dev
```

Then point your Helius webhook at `http://<your-host>:$PORT/webhook`
filtered on program `{program_id}`.

## Deploy

The server is a plain Node/Express app — deploy it anywhere a long-
running HTTP process can live (Fly, Railway, Render, a VPS, etc.).

## Edit

Handler stubs live in `src/handlers.ts`. Uncomment the switch arm you
need and implement the `handleXxx` function.
"#,
        slug = ctx.project_slug,
        program_id = ctx.program_id,
    )
}

