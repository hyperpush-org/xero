//! Helius free-tier webhook handler scaffold.
//!
//! Produces an Express server that Helius can POST enhanced-webhook
//! payloads to. Each arriving transaction is normalised into the same
//! `TxContext` shape the log-parser scaffold uses, then dispatched to
//! per-instruction / per-event handler stubs.

use super::ScaffoldContext;

pub fn render_webhook_scaffold(ctx: &ScaffoldContext<'_>) -> Vec<(String, String)> {
    vec![
        ("package.json".into(), render_package_json(ctx)),
        ("tsconfig.json".into(), render_tsconfig()),
        ("src/server.ts".into(), render_server_ts(ctx)),
        ("src/handlers.ts".into(), render_handlers_ts(ctx)),
        ("src/idl.ts".into(), render_idl_ts(ctx)),
        ("README.md".into(), render_readme(ctx)),
        (".env.example".into(), render_env_example(ctx)),
        (".gitignore".into(), render_gitignore()),
    ]
}

