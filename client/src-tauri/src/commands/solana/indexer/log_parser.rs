//! Standalone TypeScript log-parser scaffold.
//!
//! Produces a minimal Node project (`pnpm install && pnpm start`) that
//! polls `getSignaturesForAddress` + `getTransaction` via
//! `@solana/web3.js`, decodes Anchor event discriminators, and invokes
//! caller-supplied handlers.
//!
//! Deliberately stays small — 1 `package.json`, 1 `tsconfig.json`, 2
//! source files. No framework, no DB wiring, so the developer can
//! glue it into whatever they run in production.

use super::ScaffoldContext;

pub fn render_log_parser_scaffold(ctx: &ScaffoldContext<'_>) -> Vec<(String, String)> {
    vec![
        ("package.json".into(), render_package_json(ctx)),
        ("tsconfig.json".into(), render_tsconfig()),
        ("src/main.ts".into(), render_main_ts(ctx)),
        ("src/handlers.ts".into(), render_handlers_ts(ctx)),
        ("src/idl.ts".into(), render_idl_ts(ctx)),
        ("README.md".into(), render_readme(ctx)),
        (".gitignore".into(), render_gitignore()),
    ]
}

